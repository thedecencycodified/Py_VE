# =================== Assignment Operators ========================
# =
a = 5
print(a)

# +=
b = 6
b += 4
print(b)

# -=
c = 5
c -= 3
print(c)

# *=
d = 4
d *= 3
print(d)

# /=
e = 8
e /= 2
print(e)

# %= remainder
f = 10
f %= 3  # 10 = 3+3+3+1 = 1
f %= 2  # 10 = 2+2+2+2+2 = 0
print(f)

# //=
g = 10
g //= 3  # 10=3+3+3+1 = 3
print(g)

# **=
h = 5
h **= 3
print(h)
