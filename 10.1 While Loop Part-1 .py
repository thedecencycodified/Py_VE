# ================= While Loop =========================
'''number = 0
while number < 10:
    print(number)
    number = number + 1'''

'''number = 0  # break statement in while loop
while number < 10:
    print(number)
    if number == 5:
        break
    number = number + 1'''

'''number = 0  # continue statement in while loop
while number < 10:
    number = number + 1
    if number == 7:
        continue
    print(number)'''

number = 0  # else statement with while loop
while number < 10:
    print(number)
    number = number + 1
else:
    print('The Program has been Executed!!')
